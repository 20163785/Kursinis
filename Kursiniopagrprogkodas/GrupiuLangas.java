    private void PridetiStudentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PridetiStudentaActionPerformed
       String grupesKodas = GrupesID.getText();
       String studentoKodas = StudentoID.getText();
       String studentoVardas = StudentoVardas.getText();
       String studentoPavarde = StudentoPavarde.getText();
       
        sist.grupesID(grupesKodas).pridetiStudenta(Integer.parseInt(studentoKodas), studentoVardas, studentoPavarde);
       db.InsertIntoStudentai(grupesKodas, Integer.parseInt(studentoKodas), studentoVardas, studentoPavarde);
       DefaultTableModel studentuLentele = (DefaultTableModel) this.lentele2.getModel();
       
       
        studentuLentele.addRow(new Object[] {grupesKodas, studentoKodas,studentoVardas, studentoPavarde});
    }//GEN-LAST:event_PridetiStudentaActionPerformed

