    private void PridetiGrupeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PridetiGrupeActionPerformed
       String stojimoMetai = StojimoMetai.getText();
       String grupesKodas = GrupesKodas.getText();
       DefaultTableModel grupiuLentele =  (DefaultTableModel) this.lentele.getModel();
        sist.pridetiGrupe(grupesKodas, Integer.parseInt(stojimoMetai));
       db.InsertIntoGrupes(grupesKodas, Integer.parseInt(stojimoMetai));
        grupiuLentele.addRow(new Object[] {grupesKodas, stojimoMetai});
        db.dbConnect();
        
    }//GEN-LAST:event_PridetiGrupeActionPerformed

